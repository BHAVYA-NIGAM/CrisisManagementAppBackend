// Helper functions
export const getAllStates = () => INDIA_LOCATIONS.states.map(s => s.name).sort();

export const getCitiesByState = (stateName) => {
  const state = INDIA_LOCATIONS.states.find(s => s.name === stateName);
  return state ? state.cities.map(c => c.name).sort() : [];
};

export const getDistrictsByCity = (stateName, cityName) => {
  const state = INDIA_LOCATIONS.states.find(s => s.name === stateName);
  if (!state) return [];
  const city = state.cities.find(c => c.name === cityName);
  return city ? city.districts.sort() : [];
};
